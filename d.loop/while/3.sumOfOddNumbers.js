/***
Subtask-1:
Display sum of all the odd numbers from 81 to 131.
*/
let i = 81;
let sum = 0;
while(i <= 131){
    console.log(i);
    sum = sum + i;
    i+=2;
}
console.log("Sum of all the odd numbers from 81 to 131:",sum);

